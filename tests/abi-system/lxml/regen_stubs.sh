/// Bare `xmlParserVersion` data symbol — what consumers reading the
/// version global directly (rather than through the
/// `__xmlParserVersion()` accessor) link against.  `Sync` is asserted
/// by hand because `*const u8` isn't `Sync` by default; the pointer is
/// immutable and points to a `'static` byte literal, so reads from any
/// thread are safe.
#[repr(transparent)]
pub struct VersionPtr(*const u8);
unsafe impl Sync for VersionPtr {}

#[allow(non_upper_case_globals)]
#[unsafe(no_mangle)]
pub static xmlParserVersion: VersionPtr =
    VersionPtr(b"20913-sup-xml-compat\0".as_ptr());

// ── plain function stubs ─────────────────────────────────────────────────
//
// Auto-generated below from `target_symbols.txt`.  Hand-written
// implementations (see `parse.rs`, `tree.rs`, `attr.rs`, `ns.rs`,
// `serialize.rs`, `init.rs`, `error.rs`) take precedence — those
// symbols are filtered out of the stub set during regeneration.

HEADER

# Collect symbols already implemented in hand-written modules.  Scan
# every .rs file in crates/compat/src/ EXCEPT stubs.rs — that file
# would chicken-and-egg itself (it's regenerated below from this same
# skip list).
SKIP="$(mktemp)"
trap 'rm -f "$SKIP"' EXIT
(
    # Scan both compat/src and tree/src: compat statically links the
    # tree crate and re-exports its `#[no_mangle]` symbols
    # (xmlStringText, xmlStringTextNoenc) into the cdylib, so they count
    # as implemented and must not be re-stubbed.
    # `find` rather than a hardcoded list so adding a new module
    # (strutil.rs, etc.) automatically gets its functions excluded
    # from the stub set.  Only count functions that are actually
    # exported as C symbols — those decorated with `#[no_mangle]` or
    # `#[unsafe(no_mangle)]`.  A bare `pub extern "C" fn foo()` is
    # still name-mangled by Rust, so it won't satisfy a dyld
    # reference to `_foo` and listing it in the exported-symbols
    # file makes the linker error out.  Common case where this
    # matters: the SAX `noop_*` callbacks in saxreplay.rs are used
    # only as Rust-internal function pointers handed to lxml, not as
    # exported C symbols.
    find "$REPO/crates/compat/src" "$REPO/crates/tree/src" \
         -maxdepth 1 -name '*.rs' ! -name 'stubs.rs' -print0 \
        | xargs -0 awk '
            # Attribute line carrying a C export.  Matches bare
            # #[no_mangle], #[unsafe(no_mangle)], and the
            # #[cfg_attr(feature = "cdylib-exports", unsafe(no_mangle))]
            # form most of the crate uses.  Anchored to `#[` so a comment
            # mentioning no_mangle does not arm.  If the fn declaration is
            # on the SAME line (e.g. `#[cfg_attr(...)] pub extern "C" fn
            # foo()`), extract it here; otherwise arm for the next line.
            /^[[:space:]]*#\[.*no_mangle/ {
                if ($0 ~ /extern[[:space:]]+"C"[[:space:]]+fn[[:space:]]+/ \
                    && match($0, /fn[[:space:]]+[A-Za-z_][A-Za-z0-9_]*/)) {
                    ident = substr($0, RSTART + 2, RLENGTH - 2);
                    sub(/^[[:space:]]+/, "", ident);
                    print ident;
                    armed = 0;
                    next;
                }
                armed = 1;
                next;
            }
            armed && /^[[:space:]]*pub[[:space:]]+(unsafe[[:space:]]+)?extern[[:space:]]+"C"[[:space:]]+fn[[:space:]]+/ {
                # Extract the identifier after `fn`.  Macro bodies
                # like `pub extern "C" fn $name()` match the outer
                # regex but contain a Rust macro placeholder we
                # cannot resolve at this scan level — those produce
                # no match here and we skip the line.
                if (match($0, /fn[[:space:]]+[A-Za-z_][A-Za-z0-9_]*/)) {
                    ident = substr($0, RSTART + 2, RLENGTH - 2);
                    sub(/^[[:space:]]+/, "", ident);
                    print ident;
                }
                armed = 0;
                next;
            }
            # Also count `pub static mut NAME` exports — libxml2 has
            # a number of fn-pointer globals (xmlMalloc, xmlFree,
            # xmlRealloc, etc.) that downstream consumers dereference
            # rather than call; those are expressed in Rust as
            # `pub static mut`, not `fn`.
            armed && /^[[:space:]]*pub[[:space:]]+static[[:space:]]+(mut[[:space:]]+)?[A-Za-z_][A-Za-z0-9_]*[[:space:]]*:/ {
                match($0, /(static[[:space:]]+(mut[[:space:]]+)?)[A-Za-z_][A-Za-z0-9_]*/);
                # The match starts at "static "; advance past it to the identifier.
                seg = substr($0, RSTART, RLENGTH);
                sub(/^static[[:space:]]+(mut[[:space:]]+)?/, "", seg);
                print seg;
                armed = 0;
                next;
            }
            # Also pick up declarative macro invocations like
            # `xslt_null_stub!(name);` — they expand to a
            # #[no_mangle] extern "C" fn under the same identifier,
            # so listing them in stubs.rs would collide at link time.
            # Match any `*_stub!(IDENT);` macro call regardless of the
            # macro prefix (covers xslt_null_stub, xslt_minus_one_stub,
            # xslt_void_stub, and any future stub-family macros).
            /^[[:space:]]*[A-Za-z_][A-Za-z0-9_]*_stub![[:space:]]*\([[:space:]]*[A-Za-z_][A-Za-z0-9_]*[[:space:]]*\)[[:space:]]*;[[:space:]]*$/ {
                match($0, /\([[:space:]]*[A-Za-z_][A-Za-z0-9_]*[[:space:]]*\)/);
                seg = substr($0, RSTART, RLENGTH);
                gsub(/[()[:space:]]/, "", seg);
                print seg;
                next;
            }
            # Any non-blank, non-attribute line cancels the arming —
            # we only credit a #[no_mangle] that directly precedes
            # the fn / static declaration.
            /^[[:space:]]*$/        { next }
            /^[[:space:]]*#\[/      { next }
            /^[[:space:]]*\/\//     { next }
                                    { armed = 0 }
        '
    # Accessor functions + data symbols emitted manually in the header
    # above.  stubs.rs is excluded from the source scan, so anything
    # the heredoc defines must be named here to land in symbols.txt.
    printf '%s\n' __htmlDefaultSAXHandler __xmlParserVersion \
                  __xmlStructuredError __xmlStructuredErrorContext \
                  __xmlGenericError    __xmlGenericErrorContext \
                  __xmlIndentTreeOutput __xmlSaveNoEmptyTags \
                  __xmlDoValidityCheckingDefaultValue \
                  __xmlLoadExtDtdDefaultValue xmlParserVersion
) | sort -u > "$SKIP"

# Generate `stub!(name);` lines for symbols not in the skip set.
# Both files are sorted; `comm -23` lists lines in $LIST that are not
# in $SKIP — exactly what we want.
comm -23 "$LIST" "$SKIP" | sed 's/^/stub!(/; s/$/);/'
} > "$STUBS_RS"

# Emit a summary line for build logs.
# grep -c exits 1 when no matches — perfectly normal when every
# symbol is covered by hand-written code.  `|| true` swallows that
# nonzero exit so `set -e` doesn't abort the rest of the script.
N_STUBS=$(grep -c "^stub!" "$STUBS_RS" || true)
echo "regenerated $STUBS_RS ($N_STUBS stub! invocations + accessors)"

# ── 2. symbols.txt (macOS exported symbols list) ─────────────────────────
# Union of: hand-implemented symbols + accessor functions + every
# entry from target_symbols.txt.  Each name gets a leading underscore.
{
    cat "$SKIP"
    cat "$LIST"
} | sort -u | sed 's/^/_/' > "$SYMS_TXT"
N_SYM=$(wc -l < "$SYMS_TXT")
echo "regenerated $SYMS_TXT ($N_SYM symbols)"

# ── 3. symbols.ld (Linux version script) ─────────────────────────────────
{
    echo "/* Auto-generated by tests/abi-system/lxml/regen_stubs.sh"
    echo " * Source of truth: target_symbols.txt + crates/compat/src/*.rs */"
    echo "SUPXML_COMPAT_2 {"
    echo "    global:"
    sed 's/^_//' "$SYMS_TXT" | sort -u | awk '{ print "        " $0 ";" }'
    echo "    local:"
    echo "        *;"
    echo "};"
} > "$SYMS_LD"
echo "regenerated $SYMS_LD"
