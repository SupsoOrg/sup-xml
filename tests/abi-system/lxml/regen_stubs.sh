#!/usr/bin/env bash
# Regenerate stub modules from the live target_symbols list.
#
# Reads:   tests/abi-system/lxml/target_symbols.txt  (one symbol per line)
# Writes:  crates/compat/src/stubs.rs                  (functions)
# Updates: crates/compat/src/symbols.txt + symbols.ld  (version scripts)

set -euo pipefail
REPO="$(cd "$(dirname "$0")/../../.." && pwd)"
LIST="$REPO/tests/abi-system/lxml/target_symbols.txt"
STUBS_RS="$REPO/crates/compat/src/stubs.rs"
SYMS_TXT="$REPO/crates/compat/src/symbols.txt"
SYMS_LD="$REPO/crates/compat/src/symbols.ld"

# ── 1. stubs.rs ──────────────────────────────────────────────────────────
{
cat <<'HEADER'
//! Auto-generated stubs for libxml2 symbols that lxml (and other
//! consumers) reference at load time, but our shim doesn't yet
//! implement.
//!
//! Regenerated by `tests/abi-system/lxml/regen_stubs.sh` from
//! `tests/abi-system/lxml/target_symbols.txt`.  Do not edit by hand —
//! re-run the script after adding symbols to the canonical list.
//!
//! # Why every stub returns `*mut c_void`
//!
//! At load time, dyld only checks symbol *names*; signatures aren't
//! part of the linkage contract.  At runtime, the System V x86_64 /
//! AAPCS aarch64 calling conventions pass arguments in caller-saved
//! registers, so a no-arg stub can be called with any argument list
//! without corrupting state — the args just sit in registers we
//! ignore.  Return value goes in `rax`/`x0`, and `NULL` (0) is the
//! same bit pattern in every pointer or integer return type the
//! caller might interpret.
//!
//! Net effect: `import lxml` succeeds.  Any actual *use* of a stubbed
//! function gets NULL back and lxml's error-checking takes over —
//! typically raising a Python exception.

#![allow(non_snake_case)]

use std::os::raw::{c_int, c_void};
use std::ptr;

/// Diagnostic hook.  When `SUPXML_TRACE_STUBS` is set in the
/// environment, every stub call writes its name to stderr — useful
/// when a consumer crashes after calling an unimplemented function
/// and we want to know which one.
#[inline]
fn record_call(name: &'static str) {
    use std::sync::atomic::{AtomicI8, Ordering};
    static TRACE: AtomicI8 = AtomicI8::new(-1);
    let on = match TRACE.load(Ordering::Relaxed) {
        -1 => {
            let on = std::env::var_os("SUPXML_TRACE_STUBS").is_some();
            TRACE.store(if on { 1 } else { 0 }, Ordering::Relaxed);
            on
        }
        1 => true,
        _ => false,
    };
    if on {
        eprintln!("[stub] {name}");
    }
}

// Unused when every target symbol is already implemented (no stubs
// generated below) — that's the healthy state, not a problem.
#[allow(unused_macros)]
macro_rules! stub {
    ($name:ident) => {
        #[unsafe(no_mangle)]
        pub unsafe extern "C" fn $name() -> *mut c_void {
            record_call(stringify!($name));
            ptr::null_mut()
        }
    };
}

// ── thread-safe accessor functions (return pointer into mutable global) ──
//
// libxml2's globals.h has a pattern where each XMLPUBVAR global has a
// matching `__name()` accessor function.  When threading support is
// compiled in (the default on macOS / most distros), the bare global
// name expands to `(*(__name()))` — so the accessor is what shows up
// as an undefined symbol in consumer code.  We back each accessor
// with a zero-initialized storage slot of the right size.

/// libxml2 `_xmlSAXHandlerV1` (parser.h): 27 fn-ptr slots + `initialized: c_uint`
/// + 4 bytes natural padding.  We never read these fields — but our
/// storage must be large enough for callers to address them without
/// running past our allocation.
#[repr(C)]
#[derive(Copy, Clone)]
struct XmlSAXHandlerV1 {
    slots: [*mut c_void; 27],
    initialized: u32,
    _pad: u32,
}
const _: () = {
    assert!(std::mem::size_of::<XmlSAXHandlerV1>() == 224,
            "xmlSAXHandlerV1 must be 224 bytes (27*8 + 4 + 4 pad)");
};

static mut HTML_DEFAULT_SAX_HANDLER: XmlSAXHandlerV1 = XmlSAXHandlerV1 {
    slots: [ptr::null_mut(); 27],
    initialized: 0,
    _pad: 0,
};

#[unsafe(no_mangle)]
pub unsafe extern "C" fn __htmlDefaultSAXHandler() -> *mut c_void {
    record_call("__htmlDefaultSAXHandler");
    let addr = &raw mut HTML_DEFAULT_SAX_HANDLER;
    addr as *mut c_void
}

/// libxml2's `__xmlParserVersion()` returns `const xmlChar **`.
/// `xmlParserVersion` is a `const char *` global — the accessor
/// returns a pointer to that global.
static mut XML_PARSER_VERSION: *const u8 =
    b"20913-sup-xml-compat\0".as_ptr();

#[unsafe(no_mangle)]
pub unsafe extern "C" fn __xmlParserVersion() -> *mut c_void {
    record_call("__xmlParserVersion");
    let addr = &raw mut XML_PARSER_VERSION;
    addr as *mut c_void
}

/// `__xmlStructuredError()` returns `xmlStructuredErrorFunc *` —
/// a pointer to a function-pointer global.  We give it a single
/// nullable slot.
static mut XML_STRUCTURED_ERROR: *mut c_void = ptr::null_mut();

#[unsafe(no_mangle)]
pub unsafe extern "C" fn __xmlStructuredError() -> *mut c_void {
    record_call("__xmlStructuredError");
    let addr = &raw mut XML_STRUCTURED_ERROR;
    addr as *mut c_void
}

/// `__xmlStructuredErrorContext()` returns `void **` — a pointer to
/// a `void *` global slot.
static mut XML_STRUCTURED_ERROR_CONTEXT: *mut c_void = ptr::null_mut();

#[unsafe(no_mangle)]
pub unsafe extern "C" fn __xmlStructuredErrorContext() -> *mut c_void {
    record_call("__xmlStructuredErrorContext");
    let addr = &raw mut XML_STRUCTURED_ERROR_CONTEXT;
    addr as *mut c_void
